# Basic Python script for testing
def add_numbers(a, b):
    """Return the sum of two numbers."""
    return a + b

def subtract_numbers(a, b):
    """Return the difference between two numbers."""
    return a - b

# Main block to execute functions
if __name__ == "__main__":
    print("Addition of 3 and 5 is:", add_numbers(3, 5))
    print("Subtraction of 10 and 4 is:", subtract_numbers(10, 4))
